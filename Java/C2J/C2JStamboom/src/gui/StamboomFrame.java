    private void UpdatePersonen()
    {
        cbPersonen.removeAllItems();
        cbGezinnen.removeAllItems();
        for (Persoon p : administratie.getPersonen()) {
            cbPersonen.addItem(p);
            
            Iterator<Gezin> g = p.getGezinnen();
            while (g.hasNext()) {
                cbGezinnen.addItem(g.next());
            }
        }
        if (cbPersonen.getItemCount() == 0) {
            cbPersonen.addItem("geen");
        }
        if (cbGezinnen.getItemCount() == 0) {
            cbGezinnen.addItem("geen");
        }
    }
    
    

