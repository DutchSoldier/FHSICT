    private void miOpslaanAlsActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_miOpslaanAlsActionPerformed
        JFileChooser chooser = new JFileChooser();
        chooser.setCurrentDirectory(new File("."));
        chooser.setFileSelectionMode(JFileChooser.SAVE_DIALOG);
        int result = chooser.showSaveDialog(this);
        
        if (result == JFileChooser.APPROVE_OPTION) 
        {
            if(chooser.getSelectedFile()!=null)
            {  
                try
                {
                    administratie.save(chooser.getSelectedFile().getAbsolutePath());
                }
                catch(Exception e)
                {
                    JOptionPane.showMessageDialog(null, e.toString());
                }
            }    
        }   
    }//GEN-LAST:event_miOpslaanAlsActionPerformed

