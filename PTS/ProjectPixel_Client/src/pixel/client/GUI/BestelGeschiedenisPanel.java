    public BestelGeschiedenisPanel() {
        initComponents();
        PersoonType type = Client.getLoggedinAccount().getType();
        
        if (type == PersoonType.FOTOPRODUCENT){     
            try {
                facturen = Client.getServerConnection().getAlleFacturen();
            } 
            catch (RemoteException ex) {
                Logger.getLogger(BestelGeschiedenisPanel.class.getName()).log(Level.SEVERE, null, ex);
            }
            
            lst_facturen.setModel(listmodel);
            listmodel.clear();
            for (Factuur f : facturen) {
                listmodel.addElement("Factuurnummer: " + f.getFactuurnummer() + "       Datum: " + f.getDatum());
            }
        }
        else if(type == PersoonType.KLANT){                
            try {
                facturen = Client.getServerConnection().getKlantFacturen(Client.getLoggedinAccount().getEmail());
            } 
            catch (RemoteException ex) {
                Logger.getLogger(BestelGeschiedenisPanel.class.getName()).log(Level.SEVERE, null, ex);
            }
            lst_facturen.setModel(listmodel);
            listmodel.clear();

            for (Factuur f : facturen) {
                listmodel.addElement("Factuurnummer: " + f.getFactuurnummer() + "       Datum: " + f.getDatum());
            }
        }
        
        resBund = ResourceBundle.getBundle("pixel.resources.language.Language", Locale.getDefault());
        UpdateLanguage();
    }

