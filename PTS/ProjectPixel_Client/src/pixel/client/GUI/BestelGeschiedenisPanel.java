    private void refreshOrderRegelList() {
        
        
        Factuur f = facturen.get(lst_facturen.getSelectedIndex());
        int factuurnummer = f.getFactuurnummer();
        
        try {
            regels = Client.getServerConnection().getOrderRegels(factuurnummer);
        } catch (RemoteException ex) {
            Logger.getLogger(BestelGeschiedenisPanel.class.getName()).log(Level.SEVERE, null, ex);
        }
        lst_orderregel.setModel(listmodel2);
        listmodel2.clear();
        for (OrderRegel r : regels) {
            listmodel2.addElement("regelnummer: " + r.getOrderregelnummer() + " prijs: " + r.getPrijs() + " productnummer: " + r.getProductnummer());
        }
        
        
    }
