    public BufferedImage getImage() {
        BufferedImage image = new BufferedImage(window.getPanel().getWidth(), window.getPanel().getHeight(), BufferedImage.TYPE_INT_RGB);
        Graphics2D graphics2D = image.createGraphics();
        window.getPanel().paint(graphics2D);
        return image;
    }
