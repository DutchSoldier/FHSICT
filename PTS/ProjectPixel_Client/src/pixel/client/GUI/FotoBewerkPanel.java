    private void btn_opslaanActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btn_opslaanActionPerformed
        try {
            Rectangle crop = window.getPanel().getCrop();
            if (Client.getServerConnection().bewerkteFotoKoppelen(Client.getLoggedinAccount().getEmail(), foto.getFotoNummer(), window.getPanel().getEffect(), (int)crop.getMinX(), (int)crop.getMinY(), (int)crop.getMaxX(), (int)crop.getMaxY())); {
                JOptionPane.showMessageDialog(this, resBund.getString("EDITED_SAVED"), resBund.getString("EDITED_SAVED_TITLE"), JOptionPane.INFORMATION_MESSAGE);
            }
        } catch (RemoteException ex) {
            Logger.getLogger(FotoBewerkPanel.class.getName()).log(Level.SEVERE, null, ex);
        }
    }//GEN-LAST:event_btn_opslaanActionPerformed

